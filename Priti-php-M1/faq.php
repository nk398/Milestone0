<h2>When should spectators wear masks?</h2>
All spectators, regardless of vaccination status, must wear a mask in attendance 
at indoor youth sports. For outdoor youth sports events, spectators who are not fully 
vaccinated should wear a mask when unable to maintain recommended physical distance of 
at least 6 feet from non-household members or when the event is held in a community with 
substantial to high transmission.

